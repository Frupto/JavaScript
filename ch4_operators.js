// operation.js

let a = 12;
let b = 4;

console.log("a =", a, "b =", b);

console.log("Addition: ", a + b);       // 16
console.log("Subtraction: ", a - b);    // 8
console.log("Multiplication: ", a * b); // 48
console.log("Division: ", a / b);       // 3
console.log("Modulus: ", a % b);        // 0 (remainder)

// comparison.js

let x = 10;
let y = 5;

console.log("x =", x, "y =", y);

console.log("x == y :", x == y);   // false (checks equality)
console.log("x != y :", x != y);   // true  (not equal)
console.log("x > y  :", x > y);    // true
console.log("x < y  :", x < y);    // false
console.log("x >= y :", x >= y);   // true
console.log("x <= y :", x <= y);   // false

